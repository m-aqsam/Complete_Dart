void main() {
  // ***************** Loops In dart *****************

  // * For Loop

  for (int i = 1; i <= 5; i++) {
    print('Iteration $i');
  }

  // * While Loop

  int count = 1;

  while (count <= 5) {
    print('Value is  $count');
    count++;
  }

  // * D0-While Loop

  int num = 1;

  do {
    print("Your num is $num");
    num++;
  } while (num <= 5);
}
